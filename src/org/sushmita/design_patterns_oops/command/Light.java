package org.sushmita.design_patterns_oops.command;

public class Light {
    public void on(){
        System.out.println("light ON!");
    }

    public void off(){
        System.out.println("light Off!");
    }

    public void dimUp(){
        System.out.println("Dim Up");
    }
    public void dimDown(){
        System.out.println("Dim Down!");
    }

}
